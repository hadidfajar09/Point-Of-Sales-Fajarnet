    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $sale = new Sale();
        $sale->id_member = null;
        $sale->id_user = Auth::user()->id;
        $sale->total_item = 0;
        $sale->total_price = 0;
        $sale->discount = 0;
        $sale->pay = 0;
        $sale->accepted = 0;
        $sale->save();

        session(['id_sale' => $sale->id]);

        return redirect()->route('transaksi.index');
    }

